// ─── Helper Functions ───────────────────────────────────────────────────────

export function getPlacesByArea(area: string): Place[] {
  const normalizedArea = area.toLowerCase()
  return SINGAPORE_PLACES.filter(p => p.area.toLowerCase() === normalizedArea)
}

export function getPlacesByAreaWithAdjacent(area: string): Place[] {
  const normalizedArea = area.toLowerCase()
  const adjacent = ADJACENT_AREAS[area] || []
  const allAreas = [area, ...adjacent].map(a => a.toLowerCase())
  return SINGAPORE_PLACES.filter(p => allAreas.includes(p.area.toLowerCase()))
}

export function getPlacesByCategory(category: Place['category']): Place[] {
  return SINGAPORE_PLACES.filter(p => p.category === category)
}

export function isPlaceOpenAt(place: Place, timeStr: string): boolean {
  // timeStr format: "HH:MM"
  const [openH, openM] = place.openHours.open.split(':').map(Number)
  const [closeH, closeM] = place.openHours.close.split(':').map(Number)
  const [checkH, checkM] = timeStr.split(':').map(Number)

  const openMinutes = openH * 60 + openM
  const closeMinutes = closeH * 60 + closeM
  const checkMinutes = checkH * 60 + checkM

  // Handle overnight hours (e.g., open: 22:00, close: 04:00)
  if (closeMinutes <= openMinutes) {
    return checkMinutes >= openMinutes || checkMinutes <= closeMinutes
  }

  return checkMinutes >= openMinutes && checkMinutes <= closeMinutes
}

export function determineMealType(timeStr: string): 'breakfast' | 'morning_break' | 'lunch' | 'afternoon_break' | 'dinner' | 'late_night' {
  const [h] = timeStr.split(':').map(Number)
  if (h < 10) return 'breakfast'
  if (h < 11) return 'morning_break'
  if (h < 14) return 'lunch'
  if (h < 17) return 'afternoon_break'
  if (h < 21) return 'dinner'
  return 'late_night'
}

export function getCategoriesForMealType(mealType: string): Place['category'][] {
  switch (mealType) {
    case 'breakfast':
      return ['hawker', 'cafe']
    case 'morning_break':
      return ['cafe', 'coworking']
    case 'lunch':
      return ['hawker', 'cafe', 'restaurant_casual', 'restaurant_business']
    case 'afternoon_break':
      return ['cafe', 'attraction', 'shopping', 'coworking']
    case 'dinner':
      return ['restaurant_casual', 'restaurant_business', 'hawker', 'bar']
    case 'late_night':
      return ['bar', 'hawker', 'restaurant_casual']
    default:
      return ['cafe', 'hawker', 'restaurant_casual']
  }
}
